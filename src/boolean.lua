-- How boolean works

var = nil

if var == true then
    print("nil is true")
end

if var == false then
    print("nil is false")
end

true_var = true

if true_var == true then
    print("true_var is true")
end

if true_var == false then
    print("true_var is false")
end

false_var = false

if false_var == true then
    print("false_var is true")
end

if false_var == false then
    print("false_var is false")
end

zero_var = 0

if zero_var == true then
    print("zero_var is true")
end

if zero_var == false then
    print("zero_var is false")
end

empty_var = ''

if empty_var == true then
    print("empty_var is true")
end

if empty_var == false then
    print("empty_var is false")
end